<h2>Programming For Everybody</h2>
<p>
This is a series of courses designed to take students from knowing nothing about programming to the point where they have the 
confidence to consider a career as a software developer, designer, or take further education.
</p>
<ul>
<li><p>
<a href="https://ihts.pr4e.com/" target="_blank">Internet History, Technology, and Security</a> (IHTS)
- A course designed to make us all better citizens of the Internet.  Available on Coursera.
</p></li>
<li><p>
<a href="https://www.py4e.com/" target="_blank">Python for Everybody (PY4E)</a>- The world's most successful online Python course.  Available on Coursera, 
edX, FutureLearn, FreeCodeCamp, and
<a href="https://online.umich.edu/series/python-for-everybody/" target="_blank">free to University of Michigan Students</a>.
</p></li>
<li><p>
<a href="https://www.dj4e.com/" target="_blank">Django for Everybody (DJ4E)</a> - Covers
Django, HTML, CSS, SQL, JavaScript, BootStrap, and JQuery.  Available on Coursera, edX, FutureLearn, FreeCodeCamp and
<a href="https://online.umich.edu/series/django/" target="_blank">free to University of Michigan Students</a>.
</p></li>
<li><p>
<a href="https://www.wa4e.com/" target="_blank">Web Applications for Everybody (WA4E)</a> - Covers
HTML, CSS, PHP, SQL, JavaScript, BootStrap, and JQuery.  Available on Coursera, FreeCodeCamp, and 
<a href="https://online.umich.edu/series/web-applications-for-everybody/" target="_blank">
free to University of Michigan Students</a>.
</p></li>
<li><p>
<a href="https://www.pg4e.com" target="_blank">PostgreSQL for Everybody (PG4E)</a> 
A series of four courses covering PostgreSQL.  Available on Coursera, edX,
and
<a href="https://online.umich.edu/series/postgresql-for-everybody/" target="_blank">free to University of Michigan Students</a>.
</p></li>
<li><p>Under Development:
<a href="https://www.cc4e.com" target="_blank">C Programming - A Historical Perspective (CC4E)</a> 
A course looking at the C language and its profound effect on modern computing and Computer Science.
</p></li>
<li><p>
Future course topics include Computer Architecture and Java.
</p></li>
</ul>
</p>
<p>
The University of Michigan has a wide range of additional open online courses available at 
<a href="https://online.umich.edu" target="_blank">https://online.umich.edu</a>.
</p>
<p>
The materials in these courses are available under a Creative Commons License to allow for teachers 
to make use of these materials in their own courses.
Some of these
courses are made available as part of a packaged curriculum to small Liberal Arts universities for direct adoption through
<a href="https://www.thelcmc.org/programs/computer-science" target="_blank">
Lower Cost Models for Independent Colleges Consortium (LCMC)</a> and
<a href="https://rize.education/" target="_blank">Rize Education</a>.
</p>
